// In this Kata we are passing a number (n) into a function.
// Parameters: input (n) will be a number. can be pos, neg or float
// Return: true or false depending if (n) is even or not
// Example: input - testEven(2) return - true
// Pseudo Code: return a statement of n % 2 explicitly equal to 0


function testEven(n) {
    return n % 2 === 0
}

testEven(2)